<?php

return [

    /*
    |--------------------------------------------------------------------------
    | Email System
    |--------------------------------------------------------------------------
    |
    | Whether to enable the email system (e.g. when a new account is created)
    | or not. This is enabled by default.
    |
    | Requires setting up a mail provider through ./config/mail.php
    |
    */
   
    'emails' => true,

    /*
    |--------------------------------------------------------------------------
    | Experience Formula
    |--------------------------------------------------------------------------
    |
    | ...
    |
    */

    'formula-experience' => function ($level) {
        return (50 / 3) * (pow($level, 3) - (6 * pow($level, 2)) + (17 * $level) - 12);
    },

    /*
    |--------------------------------------------------------------------------
    | Magic Level Formula
    |--------------------------------------------------------------------------
    |
    | ...
    |
    */
   
    'formula-maglevel' => false,

    /*
    |--------------------------------------------------------------------------
    | Health Formula
    |--------------------------------------------------------------------------
    |
    | ...
    |
    */
   
    'formula-health' => function ($level, $value) {
        return 145 + ($level >= 8 ? (($level - 8) * $value) + (5 * 8) : (5 * $level));
    },

    /*
    |--------------------------------------------------------------------------
    | Mana Formula
    |--------------------------------------------------------------------------
    |
    | ...
    |
    */
   
    'formula-mana' => function ($level, $value) {
        return ($level >= 8 ? (($level - 8) * $value) + (5 * 8) : (5 * $level));
    },

    /*
    |--------------------------------------------------------------------------
    | Capacity Formula
    |--------------------------------------------------------------------------
    |
    | ...
    |
    */
   
    'formula-capacity' => function ($level, $value) {
        return 390 + ($level >= 8 ? (($level - 8) * $value) + (10 * 8) : (10 * $level));
    },

];
